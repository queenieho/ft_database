#include "ft_db.h"

int	ft_numlen(int n)
{
	int len;

	len = 0;
	if (n == 0)
		len = 1;
	if (n == -2147483648)
		len = 11;
	if (n < 0 && n > -2147483648)
	{
		n = -n;
		len++;
	}
	while (n > 0)
	{
		n = n / 10;
		len++;
	}
	return (len);
}
